    private void labelModifyTestDeveloperMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelModifyTestDeveloperMouseClicked
        String testDeveloperId=textTestDeveloperId.getText();
        String testDeveloperName=textTestDeveloperName.getText();
        String nic=textNIC.getText();
        int telephoneNo=Integer.parseInt(textTelephoneNo.getText());
        TestDeveloperDTO testDeveloper=new TestDeveloperDTO(testDeveloperId, testDeveloperName, nic, telephoneNo);
        try {
            TestDeveloperController testDeveloperController=ServerConnector.getServerConnector().getTestDeveloperController();
            boolean isModify=testDeveloperController.modifyTestDeveloper(testDeveloper);
            if (isModify) {
                JOptionPane.showMessageDialog(this, "Test developer detail modify successfully!");
            }
        } catch (NotBoundException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                TestDeveloperController testDeveloperController=ServerConnector.getServerConnector().getTestDeveloperController();
                testDeveloperController.releaseTestDeveloper(testDeveloperId);
            } catch (NotBoundException ex) {
                Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (MalformedURLException ex) {
                Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (RemoteException ex) {
                Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_labelModifyTestDeveloperMouseClicked

