    private void searchTestDeveloperById() {
        String testDeveloperId=textTestDeveloperId.getText();
        try {
            TestDeveloperController testDeveloperController=ServerConnector.getServerConnector().getTestDeveloperController();
            if (this.testDeveloperId!=testDeveloperId) {
                testDeveloperController.releaseTestDeveloper(testDeveloperId);
            }
            this.testDeveloperId=testDeveloperId;
            TestDeveloperDTO testDeveloper=testDeveloperController.search(testDeveloperId);
            if (testDeveloper!=null) {
                if (testDeveloperController.reserveTestDeveloper(testDeveloperId)) {
                    textTestDeveloperName.setText(testDeveloper.getTestDeveloperName());
                    textNIC.setText(testDeveloper.getNic());
                    textTelephoneNo.setText(Integer.toString(testDeveloper.getTelephoneNo())); 
                } else {
                    JOptionPane.showMessageDialog(this, "This test developer is already reserved!");
                }                 
            } else {
                JOptionPane.showMessageDialog(this, "Not found test developer!");
            }
        } catch (NotBoundException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void searchTestDeveloperByName() {
        String testDeveloperName=textTestDeveloperName.getText();
        int index=testDeveloperName.length();
        try {
            TestDeveloperController testDeveloperController=ServerConnector.getServerConnector().getTestDeveloperController();
            TestDeveloperDTO testDeveloper=testDeveloperController.searchByNameTestDeveloper(testDeveloperName);
            if (this.testDeveloperId!=testDeveloper.getTestDeveloperId()) {
                testDeveloperController.releaseTestDeveloper(testDeveloperId);
            }
            this.testDeveloperId=testDeveloper.getTestDeveloperId();
            if (testDeveloper!=null) {
                if (testDeveloperController.reserveTestDeveloper(testDeveloperId)) {
                    textTestDeveloperId.setText(testDeveloper.getTestDeveloperId());                
                    textTestDeveloperName.setText(testDeveloper.getTestDeveloperName());
                    textTestDeveloperName.select(index, 30);
                    textNIC.setText(testDeveloper.getNic());
                    textTelephoneNo.setText(Integer.toString(testDeveloper.getTelephoneNo()));    
                } else {
                    JOptionPane.showMessageDialog(this, "This test developer is already reserved!");
                }                            
            } else {
                JOptionPane.showMessageDialog(this, "Not found test developer!");
            }
        } catch (NotBoundException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (MalformedURLException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (RemoteException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(TestDeveloperManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (StringIndexOutOfBoundsException ex){}
    }
